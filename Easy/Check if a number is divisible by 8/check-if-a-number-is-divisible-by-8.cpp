class Solution{
    public:
    int DivisibleByEight(string s){
        //code here
        int k;
        if(s.size()>=3)
         k=stoi(s.substr(s.size()-3,3));
         else
         k=stoi(s);
        if(k==0||k%8==0)return 1;
        return -1;
    }
};

